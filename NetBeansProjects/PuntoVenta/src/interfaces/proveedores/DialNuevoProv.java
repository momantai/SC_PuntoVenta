    public void darValores(String prover){
        Control cont = new Control();
        ResultSet obtener=cont.mostrarProveedorFil(prover);
        if(obtener!=null){
            try{
                while(obtener.next()){
                    txtEmpresa.setText(obtener.getString(1));
                    txtRFC.setText(obtener.getString(2));
                    txtTelefono.setText(obtener.getString(3));
                }
                provedor=prover;
                btnAceptar.setText("Modificar");
            } catch(SQLException e){
                System.out.println("Error al cargar los datos "+e);
            }
        }
    }

