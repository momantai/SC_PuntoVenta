    private void txtTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoKeyTyped
        // TODO add your handling code here:
        char tecla=evt.getKeyChar();
        if(txtTelefono.getText().length()>=10){
            evt.consume();
        }else{
            if(!Character.isDigit(tecla)){
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtTelefonoKeyTyped

    private void txtRFCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRFCKeyTyped
        // TODO add your handling code here:
        char tecla=evt.getKeyChar();
        if(txtRFC.getText().length()>=13){
            evt.consume();
        }else if(!Character.isLetterOrDigit(tecla)){
            evt.consume();
        }
    }//GEN-LAST:event_txtRFCKeyTyped

    private void txtEmpresaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEmpresaKeyTyped
        // TODO add your handling code here:
        char tecla=evt.getKeyChar();
        if(Character.isLetterOrDigit(tecla) || Character.isWhitespace(tecla)){
            if(txtEmpresa.getText().length()<1){
                if(Character.isWhitespace(tecla)){
                    evt.consume();
                }
            }
        }else{
            evt.consume();
        }
    }//GEN-LAST:event_txtEmpresaKeyTyped

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if(txtEmpresa.getText().equals("") & txtRFC.getText().equals("") & txtTelefono.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos");
        }else{
            Proveedor prove = new Proveedor();
            prove.setNombreEmpresa(txtEmpresa.getText());
            prove.setRfc(txtRFC.getText());
            prove.setTelefono(txtTelefono.getText());
            if(btnAceptar.getText().equals("Agregar")){
                Control contro = new Control();
                if(contro.agregarProveedor(prove)==1){
                    JOptionPane.showMessageDialog(this, "Proveedor Agregado");
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Error al cargar");
                }
            }else{
                Control contro = new Control();
                if(contro.modificarProveedor(prove, provedor)==1){
                    JOptionPane.showMessageDialog(this, "Proveedor modificado con √©xito");
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Error al modificar");
                }
            }    
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

